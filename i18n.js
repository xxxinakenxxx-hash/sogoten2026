// 翻訳取得ヘルパー：tr('key', ...args)。関数値は差し込み、未定義キーはjaにフォールバック。
function tr(key) {
  const args = Array.prototype.slice.call(arguments, 1);
  const dict = T[lang] || T.ja;
  let v = (dict[key] !== undefined) ? dict[key] : T.ja[key];
  if (v === undefined) return '';
  return (typeof v === 'function') ? v.apply(null, args) : v;
}

